    private void btnEnviarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarDatosActionPerformed
        //los textfield hechos string 
        String TipoOrden = txtTipoOrden.getText().trim();
        String Responsable = txtResponsable.getText().trim();
        String descripcionProducto = txtProducto.getText().trim();
        String stringCantidad = txtCantidad.getText().trim();
        String ubicacion = txtUbicacion.getText().trim();
        
        
    int cantidad = 0; 
        try
        {   
            cantidad = Integer.parseInt(stringCantidad);//tranformar la cantidad de string a int
            if (cantidad <= 0) 
            {
                JOptionPane.showMessageDialog(null, "La cantidad debe ser un nmero mayor que cero.", "Error de Validacin", JOptionPane.ERROR_MESSAGE);
                return ;
            }
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Error: Ingrese un valor numrico vlido para la cantidad.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }
           
        int codigoUbicacion; 
        codigoUbicacion = Integer.parseInt(ubicacion);

        
        try    
        {   //inicializo la ubicacion para un movimiento interno manual porque no se usa
            int uDestino= 0;
            //mtodo que recibe tipos String y se encarga de buscar el objeto.
            Orden nuevaOrden = controlador.crearNuevaOrden(     
            descripcionProducto, 
            cantidad, 
            Responsable, 
            codigoUbicacion, 
            TipoOrden, 
            uDestino
            );
        } 
        catch (IllegalArgumentException e) 
        {
            // Captura el error de Producto No Encontrado o cualquier otra validacin interna.  
            JOptionPane.showMessageDialog(this, "error"); 
        }
        
    }//GEN-LAST:event_btnEnviarDatosActionPerformed

